        System.out.println("int: " + Integer.SIZE + "bit(s), " + Integer.BYTES + "byte(s)");
        System.out.println("long: " + Long.SIZE + "bit(s), " + Long.BYTES + "byte(s)");
        System.out.println("float: " + Float.SIZE + "bit(s), " + Float.BYTES + "byte(s)");
        System.out.println("double: " + Double.SIZE + "bit(s), " + Double.BYTES + "byte(s)");
//        System.out.println("boolean: " + Boolean.SIZE + "bit(s), " + Boolean.BYTES + "byte(s)");
        System.out.println("char: " + Character.SIZE + "bit(s), " + Character.BYTES + "byte(s)");

        // boolean 类型特殊
        // boolean类型参考：https://blog.csdn.net/amoscn/article/details/97377833

